/**
 * The NodeStatus enum represents the status of a node.
 */
public enum NodeStatus {

    /**
     * The node is inactive.
     */
    INACTIVE,

    /**
     * The node is active.
     */
    ACTIVE,

    /**
     * The node is unresponsive.
     */
    UNRESPONSIVE
}
